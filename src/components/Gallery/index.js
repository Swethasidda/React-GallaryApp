class Gallery extends Component {
  state = {activeImageId: imagesList[0].id}

  clickImageItem = id => {
    this.setState({activeImageId: id})
  }

  getFilteredImage = () => {
    const {activeImageId} = this.state
    const filteredImage = imagesList.filter(each => each.id === activeImageId)
    return filteredImage
  }

  render() {
    const {activeImageId} = this.state
    const filteredImage = this.getFilteredImage()
    const {imageUrl, imageAltText} = filteredImage[0]
    // console.log(imageAltText)
    // console.log(imageUrl)
    return (
      <div className="big-container">
        <img src={imageUrl} alt={imageAltText} className="small-container" />
        <h1 className="heading">Nature Photography</h1>
        <p className="para">Nature Photography by Rahul</p>
        <ul className="list-container">
          {imagesList.map(each => (
            <ThumbnailItem
              imagesList={each}
              key={each.id}
              clickImageItem={this.clickImageItem}
              isActive={activeImageId === each.id}
            />
          ))}
        </ul>
      </div>
    )
  }
}

export default Gallery
